#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;


int main()
{
    int n, temp;
    cin >> n;
    vector<int> a(n);
    vector<int> sort_a(n);

    // Вводим вектор
    for (short i = 0; i < n; ++i)
    {
        cin >> temp;
        a[i] = temp;
        sort_a[i] = temp;
    }
    






    return 0;
}
